    {
        $customer = auth('api')->user();
        $idCustomer = $customer->id;
        $customer = Customer::findOrFail($idCustomer);
        return response()->json(['success' => true, 'message' => 'successful', 'customer' => isset($customer) ? $customer : []], 200);
    }

    public function getCookie(Request $request)
    {
        $value = $request->cookie('token');
        return response()->json(['success' => true, 'message' => 'successful', 'token' => isset($value) ? $value : []], 200);
    }

